drop table document;
drop TABLE label;
drop TABLE annotation;


CREATE TABLE "document" (
"id"	INTEGER,
"filename" TEXT NOT NULL,
PRIMARY KEY("id")
);
 
CREATE TABLE "annotation" (
	"id"	INTEGER,
	"documentid" INTEGER NOT NULL,
	"username"	TEXT,
	"created" TEXT DEFAULT(datetime('now')),
	PRIMARY KEY("id"),
	FOREIGN KEY(documentid) REFERENCES document(id)
);

CREATE TABLE "label" (
	"id"	INTEGER,
	"label"	TEXT NOT NULL,
	"startpage"	INTEGER DEFAULT 0,
	"endpage"	INTEGER DEFAULT 0,
	"annotationid"	INTEGER NOT NULL,
	PRIMARY KEY("id"),
	FOREIGN KEY(annotationid) REFERENCES annotation(id)
);

CREATE VIEW IF NOT EXISTS "labeling" as
	SELECT d.id as docid, d.filename as docfn, l.label, l.startpage, l.endpage, a.id as annotation, a.username, a.created
 	FROM label l, annotation a, document d
 	WHERE 
  	  a.documentid = d.id AND
  	  l.annotationid = a.id
;


